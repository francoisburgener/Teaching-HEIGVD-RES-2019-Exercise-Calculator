public class Operation {
	
	public double add(float number, float number2){
		return number + number2;
	}
	
	public double sub(float number, float number2){
		return number - number2;
	}
	
	public double div(float number, float number2){
		return number / number2;
	}
	
	public double mul(float number, float number2){
		return number * number2;
	}
	
	
	
}
